// 🔊 Function to Play a Song
const playSong = (song, songItem) => {
  console.log("Playing:", song.title);

  // Remove all previous "Now Playing" texts & audio players
  document.querySelectorAll('.playing-text').forEach(text => text.remove());
  document.querySelectorAll('audio').forEach(audio => audio.remove());

  // Create new audio player
  let audioPlayer = document.createElement('audio');
  audioPlayer.controls = true;
  audioPlayer.src = song.src;
  audioPlayer.autoplay = true;
  audioPlayer.style.marginLeft = '15px';

  // Show Now Playing Text
  let playingText = document.createElement('p');
  playingText.textContent = `🎵 Now Playing: ${song.title}`;
  playingText.style.color = 'green';
  playingText.style.fontWeight = 'bold';
  playingText.style.margin = '5px 0 0 10px';
  playingText.classList.add('playing-text');

  // Append only to the selected song row
  songItem.appendChild(playingText);
  songItem.appendChild(audioPlayer);
};

// 🎼 Function to Add to Playlist
const addToPlaylist = (song) => {
  let playlist = JSON.parse(localStorage.getItem("playlist")) || [];
  
  if (!playlist.some(s => s.title === song.title)) {
      playlist.push(song);
      localStorage.setItem("playlist", JSON.stringify(playlist));
      alert(`${song.title} added to Playlist!`);
  } else {
      alert(`${song.title} is already in Playlist!`);
  }
};

// ❤️ Function to Add to Favorites
const addToFavorites = (song) => {
  let favorites = JSON.parse(localStorage.getItem("favorites")) || [];
  
  if (!favorites.some(s => s.title === song.title)) {
      favorites.push(song);
      localStorage.setItem("favorites", JSON.stringify(favorites));
      alert(`${song.title} added to Favorites!`);
  } else {
      alert(`${song.title} is already in Favorites!`);
  }
};

